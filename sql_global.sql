Решения по вентиляции и кондиционированию:

Для обеспечения требуемых параметров микроклимата объект был оснащен системой общеобменной вентиляции и системой кондиционирования. В помещениях магазина предусмотрены следующие системы:
– Вытяжная система c расходом воздуха 2700м3/ч;
– Приточная система;
– Система кондиционирования;
Система кондиционирования (100% рециркуляции) с расходом воздуха на средней скорости.
Холодильная мощность системы кондиционирования от ТЦ составляет 31.1 кВт.
Тепловая мощность системы кондиционирования от ТЦ составляет 13 кВт.
Слив конденсата осуществляется следующим образом: при помощи помп Sauermann SI82 вода поднимается от внутреннего блока кондиционера до магистрального трубопровода из ППР, и далее, под уклоном 0,01 в сторону существующей дренажной системы.

Оборудование систем кондиционирования:

Система кондиционирования реализуется на базе мультизональной системы К1 марки LG c внутренними блоками кассетного типа, а также от системы чиллер-фанкойл с внутренними блоками кассетного и настенного типов фирмы Carrier.
Холодоносителем в системе К1 является фреон R410A, холодоносителем в системе чиллер-фанкойл является вода с параметрами 10/16 гр.С (теплый период года), теплоносителем в системе чиллер-фанкойл является вода с параметрами 60/40 (холодный период года).
Регулирование скорости вращение вентилятора производится с индивидуального пульта управления, расположенного на уровне 1,5 м от пола на одной из колонн.
Для обеспечения в рабочей зоне нормируемой скорости приточного и вытяжного воздуха систем вентиляции, используются высокоэффективные воздухораспределители потолочного типа: 4АПН в торговом зале, диффузоры ДПУ – в подсобных помещениях и электрищитовой, все воздухораспределители фирмы “Арктос”.
В тех помещениях, где есть подвесной потолок (ГКЛ, ОГКЛ, “Армстронг”), диффузоры и воздухораспределительные решетки смонтированы в плоскость потолка.

Воздуховоды:

Применены жесткие воздуховоды из тонколистовой оцинкованной стали толщиной по СП 60.13330.2012 класса “Н”.
Монтаж систем произведен в соответствии с СП 73.13330.2012 «Внутренние санитарно-технические системы», СП 12.136.2002 «Безопасность труда в строительстве. Часть2. Строительное производство».
Стыки воздуховодов проклеены металлической лентой для обеспечения герметичности.
Воздуховоды круглого сечения закреплены к перекрытию с помощью шпилек и хомутов для воздуховодов с шагом 3600 мм. Камеры статического давления закреплены к воздуховоду, воздуховод закреплен на ближайшем горизонтальном участком максимально близко к КСД, а также к перекрытию с помощью шпилек и трапециевидных подвесов.
Хомуты для крепления воздуховодов плотно прилегают к воздуховодам, не повреждая и не деформируя изоляции.

Трубопроводы:

Трубопроводы систем холодоснабжения применены из полипропиленовых труб PPRC PN20. Трубопроводы теплоснабжения – из полипропиленовых труб PPRC PN25. Трубопроводы систем отвода конденсата – из труб PPRC PN10.
Фреонопроводы систем кондиционирования применены из медных труб. Участки капиллярных трубок от дренажных помп проложены строго вертикально. Соединение трубопроводов выполнено пайкой. Трубопроводы холодоснабжения, теплоснабжения и системы дренажа изолированы материалами на основе вспененного каучука типа Armaflex ACE 13мм и Armaflex ACE 9мм соответственно. На фитингах предусмотрена самоклеящаяся изоляция из материалов на основе вспененного каучука типа Armaflex ACE 6 мм.
Все теплоизоляционные материалы пригодны к использованию в общественных зданиях и сооружениях и имеют соответствующие сертификаты.
