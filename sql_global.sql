3.2. Параметры теплоносителя – вода с температурой  95°С-70°С.
3.3. Выделенная тепловая мощность составляет – 280кВт.
3.4. Трубопроводы применены металлические по ГОСТ 3262-75.
3.5. Теплоизоляция применена компании K-Flex ST-6мм.

