Вентиляция:

Нашей компанией были успешно произведены работы по поставке и монтажу приточно-вытяжного вентиляционного оборудования. Центральная приточно-вытяжная установка ПВ1, обеспечивающая воздухообмен в тренажерном зале, офисных помещениях и зале групповых программ, располагается в венткамере на кровле отм.+9,00. Приточные П2, П3 и вытяжные В2, В3 установки канального типа, работающие на мужские и женские душевые и гардеробные, располагаются в  обслуживающих ими помещениях, под потолком.
Качество приточного воздуха обеспечивается соответствующей обработкой наружного воздуха в приточных установках: очистка в воздушных фильтрах, нагрев зимой, охлаждение летом.
Вентиляционное оборудование, а также автоматика и щиты управления, применены производства компании Vertro (Россия).

Организация воздухораспределения в помещениях:

Тренажерный зал: приток воздуха осуществляется  через потолочные прямоугольные решетки, равномерно распределённые по помещению. Вытяжка – через настенные решетки, установленные вдоль всего помещения.
Зал групповых программ: приток воздуха осуществляется  через потолочные прямоугольные решетки, равномерно распределённые по помещению. Вытяжка – с помощью округлых решеток, установленных на воздуховоде, проходящим вдоль помещения.
Раздевалки: приток воздуха осуществляется через потолочные прямоугольные решетки, равномерно распределённые по помещению. Приточные решетки являются общими с системой кондиционирования. Вытяжка осуществляется через помещения душевых и санузлов, куда воздух поступает через переточные решетки, расположенные в верхней части двери.
Помещения администратора и отдел продаж: приток и вытяжка осуществляются  через округлые решетки, монтируемые на воздуховод.
Остальные помещения: приток и вытяжка осуществляются через потолочные диффузоры.
Подключение оконечных устройств системы вентиляции предусматривается гибким воздуховодом, длиной не более 1м. Для приточного воздуха предусматривается гибкий, утепленный воздуховод.
Регулирование воздушных потоков предусматривается дроссель-клапанами и встроенными регуляторами расхода воздуха в решетках.
Для предотвращения образования конденсата на стенках воздуховодов и нагревания перемещаемого подготовленного воздуха предусмотрена теплоизоляция марки K-Flex Air 6мм и K-Flex Air 25мм.
Для удаления влаги из вытяжных систем душевых на воздуховодах системы В3, В4 смонтированы каплеуловители. Воздуховоды класса В (плотные) – для избегания протечек. К каплеуловителям подведен дренажный трубопровод.
Для организации перетока воздуха между душевыми и раздевалками установлены переточные решетки над дверными проемами между раздевалками и душевыми.
Для предотвращения распространения шума и вибраций по воздуховодам и от оборудования предусмотрена теплоизоляция оборудования, расположенного под потолком, установлены шумоглушители, мягкие вставки, использованы виброизоляторы.

